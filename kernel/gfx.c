/* ── gfx_init ──────────────────────────────────────────────────────────── */

void gfx_init(void *fb_addr, uint32_t width, uint32_t height,
              uint32_t pitch, uint8_t bpp, uint32_t pixel_format)
{
    (void)bpp; /* always 32 */

    s_fb        = fb_addr;
    s_width     = width;
    s_height    = height;
    s_pitch     = pitch;
    s_pixel_fmt = pixel_format;

    /* Allocate back buffer: width * height * 4 bytes. */
    s_backbuf = (uint32_t *)kmalloc((size_t)width * height * 4U);

    serial_puts("[GFX] Init: ");
    /* Print width×height inline using serial_putc digits. */
    {
        char tmp[12];
        int n = 0;
        uint32_t v = width;
        if (v == 0) { tmp[n++] = '0'; }
        else { while (v) { tmp[n++] = (char)('0' + (int)(v % 10)); v /= 10; } }
        for (int i = n - 1; i >= 0; i--) serial_putc(tmp[i]);
    }
    serial_putc('x');
    {
        char tmp[12];
        int n = 0;
        uint32_t v = height;
        if (v == 0) { tmp[n++] = '0'; }
        else { while (v) { tmp[n++] = (char)('0' + (int)(v % 10)); v /= 10; } }
        for (int i = n - 1; i >= 0; i--) serial_putc(tmp[i]);
    }
    serial_puts(" fmt=");
    serial_puts(pixel_format == 0 ? "BGR" : "RGB");
    serial_puts(", backbuf ");
    serial_puts(s_backbuf ? "OK" : "FAIL");
    serial_putc('\n');
}

/* ── gfx_flush ─────────────────────────────────────────────────────────── */

void gfx_flush(void)
{
    if (!s_backbuf || !s_fb) return;

    uint8_t       *src      = (uint8_t *)s_backbuf;
    uint8_t       *dst      = (uint8_t *)s_fb;
    uint32_t const row_bytes = s_width * 4U;

    /*
     * The back buffer uses stride = width*4 (no padding).
     * The hardware framebuffer may have a larger pitch (aligned scanlines).
     * Copy row by row to handle the difference.
     */
    for (uint32_t y = 0; y < s_height; y++)
        memcpy(dst + (uint64_t)y * s_pitch,
               src + (uint64_t)y * row_bytes,
               row_bytes);
}

/* ── gfx_clear ─────────────────────────────────────────────────────────── */

void gfx_clear(uint32_t color)
{
    if (!s_backbuf) return;
    uint32_t native = color_native(color);
    uint32_t n      = s_width * s_height;
    for (uint32_t i = 0; i < n; i++)
        s_backbuf[i] = native;
}

/* ── gfx_put_pixel ─────────────────────────────────────────────────────── */

void gfx_put_pixel(int x, int y, uint32_t color)
{
    if (!s_backbuf) return;
    if ((unsigned)x >= s_width || (unsigned)y >= s_height) return;
    s_backbuf[(uint32_t)y * s_width + (uint32_t)x] = color_native(color);
}

/* ── gfx_fill_rect ─────────────────────────────────────────────────────── */

void gfx_fill_rect(int x, int y, int w, int h, uint32_t color)
{
    if (!s_backbuf || w <= 0 || h <= 0) return;

    /* Clip */
    if (x < 0) { w += x; x = 0; }
    if (y < 0) { h += y; y = 0; }
    if (w <= 0 || h <= 0) return;
    if ((uint32_t)x >= s_width  || (uint32_t)y >= s_height) return;
    if ((uint32_t)(x + w) > s_width)  w = (int)(s_width  - (uint32_t)x);
    if ((uint32_t)(y + h) > s_height) h = (int)(s_height - (uint32_t)y);

    uint32_t native = color_native(color);
    for (int row = 0; row < h; row++) {
        uint32_t *line = s_backbuf + ((uint32_t)(y + row) * s_width + (uint32_t)x);
        for (int col = 0; col < w; col++)
            line[col] = native;
    }
}

/* ── gfx_draw_rect ─────────────────────────────────────────────────────── */

void gfx_draw_rect(int x, int y, int w, int h, uint32_t color)
{
    if (w <= 0 || h <= 0) return;
    gfx_hline(x,         y,         w, color);  /* top    */
    gfx_hline(x,         y + h - 1, w, color);  /* bottom */
    gfx_vline(x,         y,         h, color);  /* left   */
    gfx_vline(x + w - 1, y,         h, color);  /* right  */
}

/* ── gfx_hline ─────────────────────────────────────────────────────────── */

void gfx_hline(int x, int y, int len, uint32_t color)
{
    if (!s_backbuf || len <= 0) return;
    if ((unsigned)y >= s_height) return;
    if (x < 0) { len += x; x = 0; }
    if (len <= 0) return;
    if ((uint32_t)x >= s_width) return;
    if ((uint32_t)(x + len) > s_width) len = (int)(s_width - (uint32_t)x);

    uint32_t native = color_native(color);
    uint32_t *row   = s_backbuf + (uint32_t)y * s_width + (uint32_t)x;
    for (int i = 0; i < len; i++)
        row[i] = native;
}

/* ── gfx_vline ─────────────────────────────────────────────────────────── */

void gfx_vline(int x, int y, int len, uint32_t color)
{
    if (!s_backbuf || len <= 0) return;
    if ((unsigned)x >= s_width) return;
    if (y < 0) { len += y; y = 0; }
    if (len <= 0) return;
    if ((uint32_t)y >= s_height) return;
    if ((uint32_t)(y + len) > s_height) len = (int)(s_height - (uint32_t)y);

    uint32_t native = color_native(color);
    for (int i = 0; i < len; i++)
        s_backbuf[((uint32_t)(y + i)) * s_width + (uint32_t)x] = native;
}

/* ── gfx_blit ──────────────────────────────────────────────────────────── */

void gfx_blit(int dst_x, int dst_y,
              const uint32_t *src, int src_w, int src_h)
{
    if (!s_backbuf || !src || src_w <= 0 || src_h <= 0) return;

    /* Clipping: compute visible rectangle in destination coordinates. */
    int sx = 0, sy = 0;
    int dw = src_w, dh = src_h;

    if (dst_x < 0) { sx = -dst_x; dw += dst_x; dst_x = 0; }
    if (dst_y < 0) { sy = -dst_y; dh += dst_y; dst_y = 0; }
    if (dw <= 0 || dh <= 0) return;
    if ((uint32_t)dst_x >= s_width  || (uint32_t)dst_y >= s_height) return;
    if ((uint32_t)(dst_x + dw) > s_width)  dw = (int)(s_width  - (uint32_t)dst_x);
    if ((uint32_t)(dst_y + dh) > s_height) dh = (int)(s_height - (uint32_t)dst_y);

    for (int row = 0; row < dh; row++) {
        const uint32_t *srow = src + (uint32_t)(sy + row) * (uint32_t)src_w + (uint32_t)sx;
        uint32_t       *drow = s_backbuf + (uint32_t)(dst_y + row) * s_width + (uint32_t)dst_x;
        for (int col = 0; col < dw; col++)
            drow[col] = srow[col];
    }
}

/* ── gfx_draw_char ─────────────────────────────────────────────────────── */

void gfx_draw_char(int x, int y, char c, uint32_t fg, uint32_t bg)
{
    if (!s_backbuf) return;

    uint8_t ch = (uint8_t)c;
    const uint8_t *glyph = g_font8x16 + (uint32_t)ch * 16U;
    uint32_t native_fg = color_native(fg);
    uint32_t native_bg = color_native(bg);

    for (int row = 0; row < 16; row++) {
        int py = y + row;
        if ((unsigned)py >= s_height) continue;
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            int px = x + col;
            if ((unsigned)px >= s_width) continue;
            uint32_t pix = (bits & (0x80U >> col)) ? native_fg : native_bg;
            s_backbuf[(uint32_t)py * s_width + (uint32_t)px] = pix;
        }
    }
}

/* ── gfx_draw_string ───────────────────────────────────────────────────── */

void gfx_draw_string(int x, int y, const char *s, uint32_t fg, uint32_t bg)
{
    if (!s || !s_backbuf) return;
    int orig_x = x;
    while (*s) {
        char c = *s++;
        if (c == '\n') {
            x  = orig_x;
            y += 16;
            continue;
        }
        gfx_draw_char(x, y, c, fg, bg);
        x += 8;
    }
}
