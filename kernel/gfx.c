/*
 * kernel/gfx.c — Kernel graphics framebuffer library.
 *
 * Double-buffered 2D drawing on top of the UEFI GOP framebuffer.
 * All draw calls write to an internal back buffer; gfx_flush() copies
 * it to the real hardware framebuffer in one memcpy-per-row.
 *
 * Constraints
 * ───────────
 * • 32 bpp only.
 * • Colors are 0xAARRGGBB (ARGB); alpha is ignored (treated as opaque).
 * • No SSE/AVX — plain C loops only.
 * • Compiled with kernel CFLAGS (-ffreestanding -mno-sse etc.).
 */

