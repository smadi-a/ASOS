/* ── Colour conversion ─────────────────────────────────────────────────── *
 *
 * Input:  0xAARRGGBB (standard ARGB, alpha ignored)
 *
 * BGR framebuffer (byte layout in memory: B G R X) — little-endian:
 *   uint32_t = X<<24 | R<<16 | G<<8 | B = 0x00RRGGBB → same as input!
 *
 * RGB framebuffer (byte layout in memory: R G B X) — little-endian:
 *   uint32_t = X<<24 | B<<16 | G<<8 | R → swap R (bits 23:16) ↔ B (7:0)
 */
static inline uint32_t color_native(uint32_t argb)
{
    argb &= 0x00FFFFFFU;
    if (s_pixel_fmt == 0) /* BGR */
        return argb;
    /* RGB: swap R and B */
    return ((argb & 0x000000FFU) << 16)
         |  (argb & 0x0000FF00U)
         | ((argb & 0x00FF0000U) >> 16);
}

