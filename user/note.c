/* ── Pixel buffer helpers ──────────────────────────────────────────────── */

static gui_window_t *s_win;

static inline void put_pixel(int x, int y, uint32_t color)
{
    if (x >= 0 && x < s_win->width && y >= 0 && y < s_win->height)
        s_win->buffer[y * s_win->width + x] = color;
}

static void fill_rect(int rx, int ry, int rw, int rh, uint32_t color)
{
    int x0 = rx < 0 ? 0 : rx;
    int y0 = ry < 0 ? 0 : ry;
    int x1 = rx + rw > s_win->width  ? s_win->width  : rx + rw;
    int y1 = ry + rh > s_win->height ? s_win->height : ry + rh;

    for (int y = y0; y < y1; y++)
        for (int x = x0; x < x1; x++)
            s_win->buffer[y * s_win->width + x] = color;
}

static void draw_char(int cx, int cy, char ch, uint32_t fg)
{
    if (ch < 0x20 || ch > 0x7E) ch = '?';
    const uint8_t *glyph = g_font8x8[ch - 0x20];

    for (int row = 0; row < FONT_H; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < FONT_W; col++) {
            if (bits & (0x80 >> col))
                put_pixel(cx + col, cy + row, fg);
        }
    }
}

/* ── Window update helper ─────────────────────────────────────────────── */

static void win_push(void)
{
    __syscall3(SYS_WIN_UPDATE,
               (uint64_t)s_win->win_id,
               (uint64_t)(uintptr_t)s_win->buffer,
               (uint64_t)((unsigned)s_win->width *
                          (unsigned)s_win->height * 4));
}

/* ── Text view rendering ──────────────────────────────────────────────── */

/*
 * draw_text_view — Clear the client area, render text_buffer with
 * newline handling, and draw a cursor line at cursor_pos.
 */
static void draw_text_view(void)
{
    /* Clear entire client area to white. */
    fill_rect(0, 0, WIN_W, WIN_H, COL_BG);

    int x = PAD_X;
    int y = PAD_Y;

    /* Track where the cursor should be drawn. */
    int cx = PAD_X;
    int cy = PAD_Y;

    for (int i = 0; i < cursor_pos || text_buffer[i] != '\0'; i++) {
        /* Record cursor screen position when we reach cursor_pos. */
        if (i == cursor_pos) {
            cx = x;
            cy = y;
        }

        /* Stop if we've reached the end of the text. */
        if (text_buffer[i] == '\0')
            break;

        if (text_buffer[i] == '\n') {
            x = PAD_X;
            y += FONT_H;
            /* Stop rendering if we go past the visible area. */
            if (y + FONT_H > WIN_H)
                break;
            continue;
        }

        /* Wrap to next line if we reach the right edge. */
        if (x + FONT_W > WIN_W - PAD_X) {
            x = PAD_X;
            y += FONT_H;
            if (y + FONT_H > WIN_H)
                break;
        }

        /* Draw the character. */
        draw_char(x, y, text_buffer[i], COL_TEXT);
        x += FONT_W;
    }

    /* If cursor is at the end of the text, record its position. */
    if (cursor_pos >= 0 && text_buffer[cursor_pos] == '\0') {
        cx = x;
        cy = y;
    }

    /* Save cursor screen coordinates for blink redraw. */
    cursor_screen_x = cx;
    cursor_screen_y = cy;

    /* Draw cursor (visible by default after a full redraw). */
    cursor_visible = 1;
    for (int dy = 0; dy < FONT_H; dy++)
        put_pixel(cx, cy + dy, COL_CURSOR);

    win_push();
}

/*
 * redraw_cursor_rect — Redraw only the cursor's rectangle.
 *
 * Erases the cursor cell to background, redraws the character underneath
 * (if any), then draws the cursor line if `show` is true.  Pushes the
 * whole buffer to the WM (the kernel does the
 * diffing/compositing).
 */
static void redraw_cursor_rect(int show)
{
    int cx = cursor_screen_x;
    int cy = cursor_screen_y;

    /* Erase the cursor cell. */
    fill_rect(cx, cy, FONT_W, FONT_H, COL_BG);

    /* Redraw the character at the cursor position (if any). */
    if (cursor_pos >= 0 && cursor_pos < TEXT_BUF_SIZE &&
        text_buffer[cursor_pos] != '\0' &&
        text_buffer[cursor_pos] != '\n') {
        draw_char(cx, cy, text_buffer[cursor_pos], COL_TEXT);
    }

    /* Draw cursor line if visible phase. */
    if (show) {
        for (int dy = 0; dy < FONT_H; dy++)
            put_pixel(cx, cy + dy, COL_CURSOR);
    }

    cursor_visible = show;
    win_push();
}

/* ── Main ───────────────────────────────────────────────────────────────── */

int main(void)
{
    /* Zero out the text buffer. */
    for (int i = 0; i < TEXT_BUF_SIZE; i++)
        text_buffer[i] = '\0';

    s_win = gui_init_window("Note", WIN_W, WIN_H);
    if (!s_win)
        return 1;

    /* Initial render. */
    draw_text_view();
    gfx_flush_display();

    int last_blink_phase = 0;

    for (;;) {
        int key = gui_poll_events(s_win);

        /* Window close event. */
        if (key == -1)
            break;

        int text_changed = 0;

        if (key > 0) {
            if (key == '\b' || key == 127) {
                /* Backspace: remove character before cursor. */
                if (cursor_pos > 0) {
                    cursor_pos--;
                    /* Shift remaining text left by one. */
                    for (int i = cursor_pos; i < TEXT_BUF_SIZE - 1; i++) {
                        text_buffer[i] = text_buffer[i + 1];
                        if (text_buffer[i] == '\0')
                            break;
                    }
                    text_changed = 1;
                }
            } else if (key == '\r' || key == '\n') {
                /* Enter: insert newline. */
                if (cursor_pos < TEXT_BUF_SIZE - 1) {
                    text_buffer[cursor_pos] = '\n';
                    cursor_pos++;
                    text_buffer[cursor_pos] = '\0';
                    text_changed = 1;
                }
            } else if (key >= 0x20 && key <= 0x7E) {
                /* Printable character: insert at cursor_pos. */
                if (cursor_pos < TEXT_BUF_SIZE - 1) {
                    text_buffer[cursor_pos] = (char)key;
                    cursor_pos++;
                    text_buffer[cursor_pos] = '\0';
                    text_changed = 1;
                }
            }
        }

        if (text_changed) {
            /* Full redraw on text change; reset blink to visible. */
            draw_text_view();
            gfx_flush_display();
            last_blink_phase = 0;
        } else {
            /* Check blink phase: 500 ms on, 500 ms off. */
            uint64_t ticks = (uint64_t)__syscall0(SYS_UPTIME);
            int phase = (int)((ticks / 500) % 2);

            if (phase != last_blink_phase) {
                last_blink_phase = phase;
                redraw_cursor_rect(phase == 0);
                gfx_flush_display();
            }
        }

        yield();
    }

    gui_destroy_window(s_win);
    return 0;
}
